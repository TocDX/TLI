// 자료형

// 값이 없다
// null (의도적으로 표현할 때 사용하는 값)
// undefined (값이 없다는 것을 확인하는 값)

let codeit;
console.log(codeit); // undefined 값이 없다

codeit = null;
console.log(codeit); // null 의도적인 값 없애기

codeit = undefined;
console.log(codeit);


let cup;
console.log(cup); // undefined
cup = '물';
console.log(cup); // 물
cup = null;
console.log(cup); // null