// 불린 (Boolean)
console.log(2 > 1);
console.log(2 < 1);
console.log(3 >= 1);
console.log(4 <= 1);
console.log(2 === 1);
console.log(2 !== 1);

// && AND 연산자
console.log(true && true)
console.log(true && false)

// || OR 연산자
console.log(true || true)
console.log(true || false)
console.log(false || false)

// ! NOT 연산자

console.log(!true)
console.log(!false)